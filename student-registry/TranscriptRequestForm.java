    private void OrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OrderActionPerformed

        try{
              String isSelected="";
              if(ExpressCheck.isSelected()) isSelected="Yes";
              else isSelected="No";
              
              TranscriptOrder TOrder = new TranscriptOrder(Integer.parseInt((String)IDText.getText().subSequence(4,8)), 
                                        FullAddressText.getText(),CourierText.getText(),isSelected);
              
              if(studentExists){
                  TOrder.Insert();
                  JOptionPane.showMessageDialog(null, "Your Request for Official Transcript has been Accepted!");
              }
              else JOptionPane.showMessageDialog(null, "Your Request didn't succeed!");

        } 
        catch (Exception e){
            System.out.println(e.toString());
        } 
    }//GEN-LAST:event_OrderActionPerformed
