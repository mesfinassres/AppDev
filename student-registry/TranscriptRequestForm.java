private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
    String IDstring=IDText.getText();
    int id=Integer.parseInt((String)IDstring.subSequence(4,8));
    
    Student student=new Student();
    try{
        Student stud=student.Search(id);
        studentExists=true;
        FullNameLabel.setText(stud.getName()+" "+stud.getFatherName()+" "+stud.getGrandFather());
        EnrolmentLabel.setText(stud.getEnrolType()+" "+stud.getEnrolYear());
    }
    catch(Exception e) {studentExists=false;}
    
}//GEN-LAST:event_SearchActionPerformed

